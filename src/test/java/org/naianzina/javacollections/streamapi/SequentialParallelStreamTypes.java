package org.naianzina.javacollections.streamapi;

public class SequentialParallelStreamTypes {

    /*
    Streams can be processed in two models: Sequential and Parallel

    - Sequential Stream: the operations are processed in a single thread, one element at a time
    This is a default behaviour for streams.

    - Parallel Stream: the operations are processed using multiple threads,
    allowing for better performance on multi-core processing.
    The sequential stream can be converted to the parallel stream by calling parallel() on the stream.


    * */
}
